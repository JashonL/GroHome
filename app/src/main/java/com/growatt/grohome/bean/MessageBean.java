package com.growatt.grohome.bean;

public class MessageBean {
    private String phoneModel;
    private String loginTime;
    private String msgContentCN;
    private String id;
    private String msgContentEN;
    private String time;
    private String title;
    private String userId;


    public String getPhoneModel() {
        return phoneModel;
    }

    public void setPhoneModel(String phoneModel) {
        this.phoneModel = phoneModel;
    }

    public String getLoginTime() {
        return loginTime;
    }

    public void setLoginTime(String loginTime) {
        this.loginTime = loginTime;
    }

    public String getMsgContentCN() {
        return msgContentCN;
    }

    public void setMsgContentCN(String msgContentCN) {
        this.msgContentCN = msgContentCN;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getMsgContentEN() {
        return msgContentEN;
    }

    public void setMsgContentEN(String msgContentEN) {
        this.msgContentEN = msgContentEN;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }
}
